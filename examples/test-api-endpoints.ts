import fetch from 'node-fetch';

interface CliArgs {
  endpoint?: 'full-crawl' | 'add-page' | 'stream' | 'info';
  url?: string;
  website?: string;
  page?: string;
  maxPages?: number;
  maxDepth?: number;
  priority?: number;
  help?: boolean;
}

function parseArgs(): CliArgs {
  const args = process.argv.slice(2);
  const parsed: CliArgs = {};
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    const nextArg = args[i + 1];
    
    switch (arg) {
      case '--help': case '-h': 
        parsed.help = true; 
        break;
      case '--endpoint': case '-e': 
        parsed.endpoint = nextArg as any; 
        i++; 
        break;
      case '--url': case '-u': 
        parsed.url = nextArg; 
        i++; 
        break;
      case '--website': case '-w': 
        parsed.website = nextArg; 
        i++; 
        break;
      case '--page': case '-p': 
        parsed.page = nextArg; 
        i++; 
        break;
      case '--max-pages': 
        parsed.maxPages = parseInt(nextArg); 
        i++; 
        break;
      case '--max-depth': 
        parsed.maxDepth = parseInt(nextArg); 
        i++; 
        break;
      case '--priority': 
        parsed.priority = parseInt(nextArg); 
        i++; 
        break;
    }
  }
  
  return parsed;
}

